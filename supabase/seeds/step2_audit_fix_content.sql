-- ------------------------------------------------------------
-- (E) 6-1-c — description 보강
-- 변경 사유:
--   - 1-4-c (의혹 기정사실화)와의 경계 명시.
--   - 6-1-c는 '특정 문장의 어휘 층위', 1-4-c는 '기사 전반의 논조'로 차이.
