UPDATE public.patterns
SET description = '검증되지 않은 의혹을 단정적인 표현으로 포장하여 마치 확인된 사실처럼 보이게 만드는 문제. ''의혹이 일고 있다'', ''의혹을 받고 있다'', ''의문도 제기된다'', ''뒷말이 무성하다'' 같은 표현. (예: ''C기업의 회계 조작 의혹이 일고 있다'') [B카테고리] 본 패턴은 기사 전체의 논조·프레임이 아닌, 특정 문장에서 의혹을 단정적으로 표현한 개별 어휘 층위의 문제에 한정한다. 기사 전반의 의혹 기정사실화는 1-4-c를 적용할 것.'
WHERE code = '6-1-c';


-- ------------------------------------------------------------
-- (F) 검증 쿼리 (적용 후 SQL Editor에서 수동 실행)
-- ------------------------------------------------------------

SELECT code, name, search_text, LEFT(description, 80) AS desc_preview
FROM public.patterns
WHERE code IN ('6-4-b','5-1-a','6-2-a','3-2-d','6-1-c')
ORDER BY code;
